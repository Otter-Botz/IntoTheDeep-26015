public class armSlide {


    ServoControllerEx armServo;
    public double angleWrap(double radians) {

        while (radians > Math.PI) {
            radians -= 2 * Math.PI;
        }
        while (radians < -Math.PI) {
            radians += 2 * Math.PI;
        }
        Math.toDegrees(angleWrap(Math.toRadians(radians)));
        return radians;

    }





}
